int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((str[i] <= 122 && str[i] >= 97) || str[i] == 32
			|| (str[i] <= 90 && str[i] >= 65))
			i++;
		else
			return (0);
	}
	return (1);
}
